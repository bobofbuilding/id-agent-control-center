#!/usr/bin/env node
/**
 * Generate src/version.ts from package.json. A compiled binary can't read
 * package.json off disk, so the version is baked in at build time. Run before
 * `bun build` (and before dev `start`) so IDCTL_VERSION is always accurate.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(resolve(ROOT, 'package.json'), 'utf8'));
const out = `// AUTO-GENERATED by build/gen-version.mjs from package.json. Do not edit by hand.
export const IDCTL_VERSION = ${JSON.stringify(pkg.version)};
export const IDCTL_BUILD_TIME = ${JSON.stringify(new Date().toISOString())};
`;
writeFileSync(resolve(ROOT, 'src/version.ts'), out);
process.stdout.write(`gen-version: src/version.ts → ${pkg.version}\n`);
